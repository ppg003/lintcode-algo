class Solution1:
    # @param {boolean[][]} grid a boolean 2D matrix
    # @return {int} an integer
    def numIslands(self, grid):
        def sameInsland(grid, position, list_position_checked):
            row = position[0]
            column = position[1]
            list_position_checked.append([row, column])
            # Right
            if column + 1 < len(grid[0]) and grid[row][column + 1] == 1 and [row,
                                                                             column + 1] not in list_position_checked:
                sameInsland(grid, [row, column + 1], list_position_checked)
            # Left
            if column - 1 >= 0 and grid[row][column - 1] == 1 and [row, column - 1] not in list_position_checked:
                sameInsland(grid, [row, column - 1], list_position_checked)
            # Up
            if row + 1 < len(grid) and grid[row + 1][column] == 1 and [row + 1, column] not in list_position_checked:
                sameInsland(grid, [row + 1, column], list_position_checked)
            # Down
            if row - 1 >= 0 and grid[row - 1][column] == 1 and [row - 1, column] not in list_position_checked:
                sameInsland(grid, [row - 1, column], list_position_checked)

        length_row = len(grid)

        if length_row == 0:
            return 0

        length_column = len(grid[0])

        if length_column == 0:
            return 0

        list_position_checked = []
        number_island = 0
        for row in range(length_row):
            for column in range(length_column):
                if grid[row][column] == 1 and [row, column] not in list_position_checked:
                    number_island += 1
                    sameInsland(grid, [row, column], list_position_checked)
        return number_island


